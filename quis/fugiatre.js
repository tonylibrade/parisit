var dt = 0;

// You can now use the variable dt in various ways:
dt = 10;  // Assign a new value to dt

// Perform operations with dt
var result = dt * 2;

// Output the result
console.log(result);  // Outputs: 20

// You can also use dt in conditional statements
if (dt > 5) {
    console.log("dt is greater than 5");
} else {
    console.log("dt is 5 or less");
}

// You can update dt based on some condition
dt += 5;  // Equivalent to dt = dt + 5

// Now dt is 15
console.log(dt);  // Outputs: 15
